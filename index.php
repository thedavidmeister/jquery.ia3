						<h2>How it works</h2>
						<p>Image-A-Trois allows you to compare a webpage agains up to three images (probably an image of the graphic design), just by clicking a bookmarklet.</p>
						<p>Why three images? Well, at first it could handle only one image, but &quot;Image-A-Un&quot; just didn't sound that catchy.<br />
							It is limited to three for that same reason.</p>
						<h4>Test your developer in only four steps</h4>
						<ul>
							<li>Install the bookmarklet, visit a webpage and click it;</li>
							<li>Specify how the page is aligned (left or centered);</li>
							<li>Select the graphic design (any image will do);</li>
							<li>Drag, scroll and find the differences.</li>
						</ul>
